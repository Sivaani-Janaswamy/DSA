public class Maths {
    public static void main(String[]args)
    {
        System.out.printf("Maximum of (23,15): %d \n",Math.max(23,15));
        System.out.printf("Minimum of (23,15): %d \n",Math.min(23,15));
        System.out.printf("Absolute value of (-15): %d \n",Math.abs(-15));
        System.out.printf("Square root of (25): %d \n",(int)Math.sqrt(25));
        System.out.printf("Random value between 1 to 10: %d \n",1+(int)(Math.random()*10));
    }
}
